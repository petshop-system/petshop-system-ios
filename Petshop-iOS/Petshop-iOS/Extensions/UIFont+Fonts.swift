import UIKit

extension UIFont {
    static func notoSansLight(size: CGFloat) -> UIFont? {
        return UIFont(name: "NotoSans-Light", size: size)
    }
    
    static func notoSansRegular(size: CGFloat) -> UIFont? {
        return UIFont(name: "NotoSans-Regular", size: size)
    }
    
    static func notoSansMedium(size: CGFloat) -> UIFont? {
        return UIFont(name: "NotoSans-Medium", size: size)
    }
    
    static func notoSansBold(size: CGFloat) -> UIFont? {
        return UIFont(name: "NotoSans-Bold", size: size)
    }
    
    static func catamaranRegular(size: CGFloat) -> UIFont? {
        return UIFont(name: "Catamaran-Regular", size: size)
    }
    
    static func catamaranMedium(size: CGFloat) -> UIFont? {
        return UIFont(name: "Catamaran-Medium", size: size)
    }
    
    static func catamaranBold(size: CGFloat) -> UIFont? {
        return UIFont(name: "Catamaran-Bold", size: size)
    }
    
    static func carroisGothicRegular(size: CGFloat) -> UIFont? {
        return UIFont(name: "CarroisGothic-Regular", size: size)
    }
}
